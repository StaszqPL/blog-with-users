@app.route("/post/<int:post_id>/<show_comment>", methods=["GET", "POST"])
def show_post(post_id, show_comment):
    form = CommentForm()
    requested_post = BlogPost.query.get(post_id)
    if form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("You need to be logged in to comment.")
            return redirect(url_for("login"))
        new_comment = Comment(
            text=form.text.data,
            author=current_user,
            post=requested_post
        )
        db.session.add(new_comment)
        db.session.commit()
    return render_template("post.html", post=requested_post, current_user=current_user, form=form, show_comment=show_comment)


